n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))
soma = n1 + n2
multiplicacao = n1 * n2
divisao = n1 / n2
divisaointeira = n1 // n2
exponenciacao = n1 ** n2
print('A soma vale {}, o produto é {}, a divisão é {:.3f}, a divisão inteira é {} e a potência é {}'.format(soma, multiplicacao, divisao, divisaointeira, exponenciacao))